/**
* Return the values of url as an array
*/
function getUrlVars(u)
{

		var pos = u.indexOf("?");
		var values = u.substr(pos+1,u.length);
		//console.log("params = "+values);

		var pars = values.split('&');
		var arr=[]

		for(i=0;i<pars.length;i++)
		{
			var tmp = pars[i].split(':');
			arr[tmp[0]]=tmp[1];
		}

		return arr;

}



function getAge(age_1,age_2)
{

	var res = age_1.split("|");

	var day = parseInt(res[0]);
	var month = parseInt(res[1])*30;
	var year = parseInt(res[2])*365;


	var entry_total = day+month+year;

	res_birth = age_2.split("|");

	 day_birth = parseInt(res_birth[0]);
	 month_birth =  parseInt(res_birth[1])*30;
	 year_birth = parseInt(res_birth[2])*365;

	var birth_entry_total  =  day_birth+month_birth+year_birth;


	var reading_age = Math.floor(((entry_total-birth_entry_total)/365)*12);

	return reading_age;
}

